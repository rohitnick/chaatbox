function minus1()
{
var x=$('#num1').val();
var y=$('#prices1').html();
var total_order=$('#total').html();
if(x!=1)
{
z=x-1;
$('#num1').val(z);
$('#pricess1').html(z*y);
$('#total').html(total_order-y);
}
}
