
function cross1()
{ 
var x=$('#num1').val();
var y=$('#prices1').html();
$('#line-items #tag1').remove();
$('#1').removeClass('js-product-selected');
$('#1').find('.js-product-details').removeClass('bg-green');
$('#total').html($('#total').html()-(x*y));
}
